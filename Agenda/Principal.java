    private void btnInsertarInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarInicioActionPerformed
       //340--260
        int Opcion = 0;
        while (Opcion == 0) {
            Nodo nuevo = new Nodo();
            try {
                nuevo.setId(Integer.parseInt(JOptionPane.showInputDialog("Id: ")));
                nuevo.setNombre(JOptionPane.showInputDialog("Nombre: "));
                nuevo.setApellido(JOptionPane.showInputDialog("Apellido: "));
                nuevo.setTelefono(Integer.parseInt(JOptionPane.showInputDialog("Telefono: ")));
                nuevo.setCorreo(JOptionPane.showInputDialog("Correo: "));
                agenda.InsertarInicio(nuevo);
                Opcion = JOptionPane.showConfirmDialog(null, "Desea agregar otro contacto?", "Lista simple enlazada", JOptionPane.YES_NO_OPTION);
                txtContactos.setText(agenda.Listar());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "No se pueden ingresar letras!", "ERROR ", 0);
            }

        }

    }//GEN-LAST:event_btnInsertarInicioActionPerformed

