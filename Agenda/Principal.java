    public void Guardar() {
        String texto = txtContactos.getText();
        File archivo = new File("Contactos.txt");
        try {//Si se encuentra el archivo a escribir
            //Crear la declaracion y ruta
            FileWriter escribiendo = new FileWriter(archivo);
            escribiendo.write(texto);
            escribiendo.write("\n");
            escribiendo.close();
        } catch (IOException ex) {//Si no se encuentra el archivo a escribir
            JOptionPane.showMessageDialog(this, "No se encontr√≥ el archivo");
        }

    }

    private void Cargar() {
        //Toma la ruta del archivo
        File archivo = new File("Contactos.txt");
        try {
            //Toma el archivo de la ruta especificada
            Scanner scan = new Scanner(archivo);
            //Lee el archivo
            while (scan.hasNext()) {//Verifica si quedan palabras por leer
                int Id = scan.nextInt();
                String Nombre = scan.next();
                String Apellido = scan.next();
                int Telefono = scan.nextInt();
                String Correo = scan.next();
                Nodo info = new Nodo(Id, Nombre, Apellido, Telefono, Correo);
                agenda.InsertarFinal(info);  
            }
            scan.close();
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "No se pudo leer");
        }
    }

